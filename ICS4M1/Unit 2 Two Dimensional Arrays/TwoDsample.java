// The "TwoDsample" class.
public class TwoDsample
{
    public static void main (String [] args)
    {
        int z = 0;

        int table [] [] = new int [3] [4];

        for (int rows = 0 ; rows < 3 ; rows++)
        {
           System.out.print ("\n");

            for (int cols = 0 ; cols < 4 ; cols++)
            {
                z++;
                table [rows] [cols] = 1 + z;
                System.out.print (table [rows] [cols] + " \t");
            }
        }
    } // main method
} // TwoDsample class

